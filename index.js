const divide = function() {
    return 2000 / 100; 
  };
  
  // 3) Arrow function called square
  const square = (num) => num * num;  // 4) Takes one parameter and multiplies it by itself
  
  // 5) Arrow function called add
  const add = (a, b) => a + b;  // 6) Takes two parameters and adds them together
  
  // Example usage:
  console.log(divide());   // Output: 20
  console.log(square(5));  // Output: 25
  console.log(add(4, 6));  